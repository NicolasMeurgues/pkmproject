package com.vogella.android.androidpkmn.model;

class Evs {

    private int hp=0;
    private int atk=0;
    private int def=0;
    private int spa=0;
    private int spdef=0;
    private int spe=0;


    public int getHp() { return hp; }
    public void setHp(int hp) { this.hp = hp; }

    public int getAtk() { return atk; }
    public void setAtk(int atk) { this.atk = atk; }

    public int getDef() { return def; }
    public void setDef(int def) { this.def = def; }

    public int getSpa() { return spa; }
    public void setSpa(int spa) { this.spa = spa; }

    public int getSpdef() { return spdef; }
    public void setSpdef(int spdef) { this.spdef = spdef; }

    public int getSpe() { return spe; }
    public void setSpe(int spe) { this.spe = spe; }
}
